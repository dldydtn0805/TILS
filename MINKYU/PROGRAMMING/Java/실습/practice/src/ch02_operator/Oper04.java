package ch02_operator;

// 연산자 - 04
// 대입 연산자
public class Oper04 {
    public static void main(String[] args) {
        // 5. 대입 연산자
        // =, +=, -=, *=, /=, %=
        // 변수에 값을 할당하기 위해 사용하는 연산자
        int a = 5;
        System.out.println(a+=1); // 5 + 1 = 6
        System.out.println(a-=2); // 6 - 2 = 4
        System.out.println(a*=3); // 4 * 3 = 12
        System.out.println(a/=4); // 12 / 4 = 3
        System.out.println(a%=5); // 3 % 5 = 3

        // 6. 삼항 연산자
        // 조건식 ? 참일경우 : 거짓일경우


    }
}
