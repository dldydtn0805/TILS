package ch01_variable;

// 변수 - 02
// 선언
public class Var02 {
    public static void main(String[] args) {
        int a;
        int b;
        // int형 데이터를 저장할 수 있는 변수 c와 d를 선언한다
        int c, d;


        // 초기화 : 선언한 변수에 처음으로 값을 저장하는 것
        a = 1;
        b = 2;
        c = 3;
        d = 4;

        // 선언과 초기화를 동시에
        int e = 5;
        // 동시에 같은 타입 변수를 선언과 초기화하기
        int f = 6, g = 7;

    }
}
